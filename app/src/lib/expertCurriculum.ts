export const buildCurriculumContext = (blueprint: CurriculumBlueprint) => {
  return {
    totalDurationMins: blueprint.modules.reduce((s, m) => s + (m.durationMins || 0), 0),
    totalObjectives: blueprint.modules.reduce((s, m) => s + m.objectives.length, 0),
    totalExercises: blueprint.modules.reduce((s, m) => s + (m.exercises?.length || 0), 0),
    totalResources: blueprint.modules.reduce((s, m) => s + (m.resources?.length || 0), 0),
    hasRichContent: blueprint.modules.some((m) => !!m.lessonSections?.length),
  }
}
